package jexcalibur.shared;


public class CTimeInterval {

	/**
	 * @uml.property  name="periodMultiplier"
	 */
	public double periodMultiplier;

	/**
	 * Getter of the property <tt>periodMultiplier</tt>
	 * @return  Returns the periodMultiplier.
	 * @uml.property  name="periodMultiplier"
	 */
	public double getPeriodMultiplier() {
		return periodMultiplier;
	}

	/**
	 * Setter of the property <tt>periodMultiplier</tt>
	 * @param periodMultiplier  The periodMultiplier to set.
	 * @uml.property  name="periodMultiplier"
	 */
	public void setPeriodMultiplier(double periodMultiplier) {
		this.periodMultiplier = periodMultiplier;
	}

	/**
	 * @uml.property  name="periodCode"
	 */
	public int periodCode;

	/**
	 * Getter of the property <tt>periodCode</tt>
	 * @return  Returns the periodCode.
	 * @uml.property  name="periodCode"
	 */
	public int getPeriodCode() {
		return periodCode;
	}

	/**
	 * Setter of the property <tt>periodCode</tt>
	 * @param periodCode  The periodCode to set.
	 * @uml.property  name="periodCode"
	 */
	public void setPeriodCode(int periodCode) {
		this.periodCode = periodCode;
	}

}
